function melee(id: string, name: string, dlc: boolean, tail: string[]): WeaponDef {
  return {
    id,
    name,
    category: 'MELEE',
    dlc,
    challenges: [...MeleeE, ...tail],
  }
}

function launcher(id: string, name: string, dlc: boolean, tail: string[]): WeaponDef {
  return {
    id,
    name,
    category: 'LAUNCHERS',
    dlc,
    challenges: [...MeleeE, ...tail],
  }
}

